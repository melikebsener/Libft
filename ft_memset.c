#include "libft.h"
#include <stdio.h>

void	*ft_memset(void *s, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		*((unsigned char *)s + i) = c;
		i++;
	}
	return (s);
}
